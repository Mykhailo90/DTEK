<p>И напоследок - цепочка вопросов, которая поможет вам создать заразительный контент для вашего бренда:</p>

<ul>
	<li>В чем суть бренда?</li>
	<li>Как мы можем сделать знакомое удивительным?</li>
	<li>Как мы можем сделать что-то запоминающимся?</li>
	<li>Как мы можем использовать эмоции, информацию и контекст?</li>
	<li>Кто наши Gatekeppers?</li>
	<li>Как мы можем охватить аудиторию нашей аудитории?</li>
</ul>
