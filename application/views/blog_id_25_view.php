<h2><strong>Как добиться заразительности?</strong></h2>

<p>В своем выступлении Brian рассказал о механизме создания заразительного контента, который он почерпнул из книги <strong>“Contagious” Jonah Berger</strong>.</p>

<p>Что должно быть в контенте, чтоб он стал заразительным?</p>

<p><strong>Социальная актуальность </strong>- мы все хотим быть классными, умными, запоминающиеся. В своем контенте дайте людям пути получить социальные символы статуса.</p>

<p>Пример: Кто из нас не любит похвастаться в кругу друзей новым уютным местечком. Бар Please don’t tell воспользовался этой страстью порекомендовать заведение: внутри обычного кафе стоит телефонная будка, совершив звонок из которой, гость попадает в “тайный бар” о котором нельзя говорить. Результат: полная посадка безо всякой рекламы.</p>

<p><strong>Триггеры </strong>- чем чаще люди думают про продукт или идею, тем чаще о нем будут говорить.</p>

<p>Пример: знаменитый батончик Kit-Kat воспользовался самым известным триггером, с которым теперь у многих ассоциируется эта шоколадка. В одной из рекламных кампаний потребителям предложили есть Kit-Kat с кофе. Этот тандем популярного напитка и батончика повысил продажи последнего в разы.</p>

<p><img alt="Kit-Kat и кофе" src="https://dtekacademy.com/img/blog/CcJlzwRWoAEYRmL.jpg"></p>

<p><strong>Эмоции</strong> - сосредоточьтесь на чувствах, а не функциях. Фокусируйтесь при создании контента именно на ярких эмоциях, тогда люди запомнят и захотят поделиться вашим контентом. Но не все эмоции работают одинаково: одни побуждают шерить контент, а другие же наоборот снижают желание делиться.</p>

<p><strong>Публичность</strong> - делайте контент более просматриваемым/заметным, а значит и популярным.</p>

<p>Пример: во время создания Macbook у дизайнеров случилась дилемма, как должно располагаться яблоко на крышке. Оно должно быть лицом к владельцу, когда закрыта крышка или лицом к людям вокруг владельца, когда он работает на ноутбуке. Выбрали второй вариант, потому что для владельца важнее, чтоб другие видели логотип.</p>

<p><strong>Практическая ценность - </strong>Если информация содержит практическую ценностью, люди будет охотно ею делиться.</p>

<p><strong>Истории</strong> - люди не делятся информацией, люди рассказывают истории. Упаковуйте идеи и продукты в истории, которые люди хотят рассказать.</p>

<p>Пример: история про Троянского коня прожила несколько тысяч лет. Вот вам яркий сторителлинг!</p>

<p><img alt="Троянский конь" src="https://dtekacademy.com/img/blog/goroda-244.jpg"></p>

