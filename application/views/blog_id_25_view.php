<h2><strong>Какой же контент считается виральным?</strong></h2>

<p>Это 1000 лайков или миллион просмотров? Это за год или неделю? Это видео или статьи? Давайте попробуем разобраться в том, что такое виральность.</p>

<p><strong>Виральность (Virality)</strong> - это поток социальной информации, когда много людей одновременно делятся контентом в течение короткого периода времени в своих социальных сетях, когда сообщение распространяется за пределы одной сети (одной группы людей) в разные, зачастую удаленные сети, что приводит к резкому росту количества людей, на которых воздействует сообщение.</p>

<p><strong>“Going Viral” Nahon and Hemsley</strong></p>

<h2><strong>Как брендам вклинится в беседы людей?</strong></h2>

<p>Каждый час в мире происходит порядка 100 млн бесед про бренды: люди обсуждают свой опыт, пересказывают истории, делятся новинками, жалуются или хвалят.</p>

<p>Каждый бренд мечтает попасть в эти 100 млн и блистать в этих беседах в своем лучшем свете. <strong>НО!</strong> Люди в первую очередь говорят не о брендах, а о себе:</p>

<ul>
	<li>⅓ времени в беседах,</li>
	<li>до 80% в онлайне.</li>
</ul>

<p>О вашем бренде могут и будут активно говорить, когда человек сможет презентовать в первую очередь себя.</p>

<p>Вокруг вирусных технологий существует миф, будто это новинки и свежий инструмент, но на самом деле методика шеринга (распространения контента) - ровесник человека:</p>

<ul>
	<li>раньше информацией делились, сидя у костра, в кругу соплеменников.</li>
	<li>сейчас информацией делятся в соцсетях через свой мобильный телефон.</li>
</ul>

<p>Масштабы разные, а технология - одна!</p>

<h2><strong>Давайте разложим механику вирусных технологий на детали</strong></h2>

<h3><strong>Контент и дистрибуция</strong></h3>

<p>Content is the King - как бы да, но король без королевства никуда. В вирусных технологиях королевство для контента - его распространение/дистрибуция.</p>

<p><img alt="Контент - Король" src="https://dtekacademy.com/img/blog/contentisking3.jpg" style="width: 500px;"></p>

<p>Есть 2 типа контента:</p>

<p>Продвигаемый и социальный. Шансы стать виральным гораздо выше у социального, так как интерес к нему живет гораздо дольше + он более естественный для людей.</p>

<h3><strong>Сети и связи</strong></h3>

<p>Сети (Networks) - это “паутина” связей вашей аудитории. Вирусный эффект - это распространение не только между большим количеством людей, но и большим количеством сетей. Оно возможно благодаря разным типам связей.</p>

<p>Сильные связи: ваши друзья, семья, коллеги, одноклассники - личная сеть.</p>

<p>Слабые связи: кластеры, сообщенные между собой “мостиками” - это целая система личных сетей.</p>

<p><img alt="Сети" src="https://dtekacademy.com/img/blog/sitenoticia15-1-680x380.jpg"></p>

<p><strong>Обратите внимание! </strong>Создавая контент для для своего бренда, делайте это не только для своей аудитории, но и для аудитории вашей аудитории, тогда возможен вирусный эффект!</p>

<h3><strong>Gatekeppers</strong></h3>

<p>Gatekeppers - это “фильтры” на пути распространения контента. Это могут быть люди, компании, учреждения, в зависимости от их расположения внутри сети, они могут продвинуть или придушить движение информации из одной части сети в другую.</p>

<p>Например, алгоритм показов контента в Facebook может как сопутствовать распространению контента, так и ограничивать его. Поэтому его стоит учитывать в дистрибуции контента.</p>

<h3><strong>Время и скорость</strong></h3>

<p>Половина ретвитов происходит в первый час, 75% в первый день. Поэтому скорость в вопросе распространения очень важна. Нет четкого ограничения, например: вирусный эффект возможен только за 1 день, но статистика показывает, что “заразительная волна”движется очень-очень быстро.</p>

