<ol>
	<li><strong>ЗАЧЕМ?</strong></li>
</ol>

<p>Собрались что-то сказать или сделать? Неплохо бы вывести себя на осознанность и ответить себе честно и прямо на этот вопрос.</p>

<ul>
	<li>С какой ЦЕЛЬЮ я это делаю/говорю?</li>
	<li>Какую хочу вызвать реакцию?</li>
	<li>Зачем это мне?</li>
	<li>Зачем это тем, кто слышит?</li>
</ul>

<ol start="2">
	<li><strong>КОМУ?</strong></li>
</ol>

<p>Кто ваша целевая аудитория? (ЦА)</p>

<p>Ну серьёзно... Мы все разные. И нам так хочется, чтобы с нами говорили на нашем понятийном языке, а сами часто это же и забываем. Изучите ваших слушателей, клиентов, друзей...</p>

<p>Понимая ответы на вопросы ЗАЧЕМ и КОМУ мы уже понимаем ответ на следующий вопрос...</p>

<ol start="3">
	<li><strong>ЧТО?</strong></li>
</ol>

<p>Контент: он важен. Будьте проще и конкретнее. Давайте "мясо" и пользу. Водичка тоже хороша для бульончика, но не перестарайтесь.</p>

<p>Говорите то, что ВАЖНО тем, с кем общаетесь. Банальная истина... Но сотни конференций показали мне обратное (((. Часто спикер говорит о том, что важно ей/ему, а не людям в зале. Хотя иногда это и совпадает )).</p>

<ol start="4">
	<li><strong>КАК?</strong></li>
</ol>

<p>Форма тоже важна. С этим согласятся не только фитнесс тренера, но и все, кто хоть раз был на докладе с интересной темой и нуднейшей подачей.</p>

<p>Есть масса инструментов подачи материала и взаимодействия с людьми, которые мы будем&nbsp;обсуждать позже. Но самые главные из них - ваша ИСКРЕННОСТЬ и СТРАСТЬ к теме.</p>

<ol start="5">
	<li><strong>КОГДА? ГДЕ? ПОСЛЕ КОГО? и пр...</strong></li>
</ol>

<p>Есть ещё и рутинные вопросы, которые помогут вам подготовиться к подаче идеи более основательно. И они тоже важны.</p>

<p><strong>НО!</strong></p>

<p>Первые 4 вопроса просто критичны. Начните с них.</p>

<p>Иногда, вовремя заданный вопрос важнее ответа.</p>

