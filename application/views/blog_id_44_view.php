                                            <li>Operations Management Director of the Baumann-Steiner Endowed Fund for Creativity</li>
                                            <li>Business Director of the Creativity-Business Learning Platform</li>
                                            <li>Director of the INSEAD-ArtCenter Collaboration INSEAD</li>
                                        </ul>

                                        <p><strong>Инновации можно исследовать</strong></p>

                                        <p>Профессор Соса в своей исследовательской деятельности сфокусирован на улучшениях инновационных возможностей компаний. В первую очередь он исследует управление дизайном и творчеством в различных организационных структурах, процесс создания инноваций в крупных компаниях. Результаты его исследований публикуются в ASME и Harvard Business Review.</p>

                                        <p><strong>Преподавательский опыт</strong></p>

                                        <p>Профессор Соса преподает в программах MBA, PhD и Executive предметы, связанные с управлением инновациями и творческим мышлением. Совместно с Колледжем дизайна Art Center (Пасадена, Калифорния) он разработал программу для интеграции дизайна и бизнес-мышления для успешных инноваций. Программа стартовала в 2005 году и с тех пор успешно работает. Мануэль Соса занял второе место в номинации «Лучший профессор» за свой курс MBA по управлению инновациями и творческому мышлению в Сингапуре в 2012 и 2013 годах.</p>

                                        <p><strong>Какую программу профессор везет в Украину?</strong></p>

