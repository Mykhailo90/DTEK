                                        <p><strong>Где учат инновациям?</strong></p>

                                        <p>Мы в Академии ДТЭК задались целью найти лучшую экспертизу в вопросах создания инноваций. Именно поэтому выбрали ведущую мировую бизнес-школу INSEAD и руководителя программы “Innovation by Design” Мануэля Сосу.</p>

                                        <p>Один из лучших мировых специалистов в создании дизайна мышления и трансформационных инноваций выступит в Киеве 23 мая с однодневной сессией.</p>

