<p>На эффективность в менеджменте часто смотрят с высоты бизнеса и его результатов: выполнила ли команда этого руководителя план, сколько денег принес департамент, какие проекты реализовали? Но KPI’s могут быть разными, цели у бизнеса могут быть разными, а вот одно условие в менеджменте остается неизменным - это команда. Руководитель всегда работает с командой!</p>

<p><strong>Как же воспринимают подчиненные своих руководителей, считают ли их эффективными, да и какие критерии являются показателями эффективности?</strong></p>

