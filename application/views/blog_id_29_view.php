<h3><strong>Где живет страх и чего мы боимся?</strong></h3>

<p>При одной мысли о том, что сейчас нужно выйти и выступить у многих в бездне подсознания просыпается и тянет свои липкие щупальца внутренний Ктулху, обволакивая сердце и сковывая намерения...</p>

<p>Причин этому страху может быть много. Но чаще всего это:</p>

<ul>
	<li>страх оценки;</li>
	<li>страх неприятия/отторжения;</li>
	<li>придача излишней важности происходящему.</li>
</ul>

<p><img alt="Уверенній спикер" src="https://dtekacademy.com/img/blog/nuf7xq5bk0fnehckn6tdxdf5fkc.jpeg"></p>

<h3><strong>Оружие против страхов выступлений</strong></h3>

<p>Мы не будем с вами искать универсальную таблетку от всех страхов или пытаться лечить все и сразу. Дальше рассматривать методы преодоления страха оценки и перевода этой разрушающей энергии в создающую.</p>

<p>Ловите один из универсальных методов, который я заметил давно, как очень практичный - <strong>позитивное якорение</strong>.</p>

<ol>
	<li>Для этого нужно достаточно часто (минимум раз в неделю) <strong>выступать, хоть на 5 минут</strong> и попросить ваших слушателей только хвалить вас за это. Аплодисменты, комплименты, эмоции. Всё, что угодно. Но только НЕ критика.</li>
	<li>Также устройте сами себе <strong>великий пост на критику </strong>в течение 40 дней и минимум 20 раз в день получайте похвалу от себя и других даже за попытки самовыражения в речи.</li>
</ol>

<p><strong>Зачем?</strong> Основная задача - выработать у себя условный рефлекс на выступления. Чтобы аж хотелось снова и снова кайфануть.</p>

<p><strong>Как начать?</strong> Первое, что можете уже сейчас сделать для себя - это хвалить себя 20 раз в день за любые экологичные проявления взаимодействия с людьми. И не критиковать себя 40 дней.</p>

