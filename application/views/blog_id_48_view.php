                                        <p>Лучшей на рынке компанию делает ее команда. Ведь компания не сможет добиться успеха, если сотрудники не смогут работать эффективно вместе.</p>

                                        <p>На поиски ответа «Что же делает команду эффективной?» отправилась транснациональная компания <strong>Google</strong>. В рамках своего проекта <strong>«Аристотель»</strong>, Google провел исследование с целью выяснить, что же влияет на эффективность работы команды. Было проведено более 200 интервью с сотрудниками компании, в ходе которых отслеживали 180 атрибутов для 180 команд. Учитывались разные факторы, которые могли бы повлиять на эффективность команды: от размера команды и социальных связей ее членов, до индивидуальных особенностей сотрудников.</p>

                                        <p>Компания Google выяснила, что ни срок пребывания сотрудника в команде, ни позиция команды в компании, ни размер рабочей нагрузки, не влияют на ее эффективность.</p>

                                        <p>Самым важным фактором, по результатам исследования Google, является <strong>ощущение сотрудниками безопасности внутри команды</strong>:</p>

