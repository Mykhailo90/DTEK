				<div class="blocks_menu" id="blocks_menu"><div class="block_area" id="block_area" style="height: auto;"><div class="swipe_block" style="transform: translateX(0%); position: relative;"><div class="blog_frame"><div class="blog_frame_940"><h1> Как получить сертификат HRCI в Украине?</h1><div class="blog_info"><div class="author"><div>Автор статьи:</div><div>Анна Яременко</div></div><div class="category"><div>Раздел:</div><div>null</div></div><div class="date"><div>Дата:</div><div>05.03.2018</div></div></div><div class="img"><div><img src="//dtekacademy.com/img/events/AP_HRBP.png" alt="Сертификация HRCI"></div></div><div class="common_text_content"><p><strong>Что такое HRCI?</strong></p>

<p><strong>HR Certification Institute (HRCI) </strong>– это передовой американский институт, который уже более 40 лет помогает HR специалистам по всему миру улучшать свою экспертизу. HRCI создают и на постоянной основе актуализируют мировые стандарты, которые являются основополагающими для успешной деятельности HR-ов. Уже более 500 тыс. специалистов и профессионалов в более чем 100 странах прошли сертификацию HRCI и подтвердили уровень своих знаний HR-процессов и процедур, необходимых для процветания любого бизнеса.</p>

<p>Переговоры о сотрудничестве Академии ДТЭК с HRCI мы начали вести ещё весной 2017 г., и уже в ноябре 2017 г. мы стали официальным партнером HRCI по двум направлениям: <strong>Certification </strong><strong>Preparation </strong><strong>Provider </strong>и <strong>Approved </strong><strong>Provider</strong>. Более детально про данные виды партнерства расскажем ниже в статье.</p>

<p><strong>Кому нужна сертификация </strong><strong>HRCI?</strong></p>

<p>Всем HR-ам, которые заинтересованы в том, чтобы идти нога в ногу с мировыми практиками, быть частью международного HR сообщества, расти как профессиональный HR, да и как минимум определить уровень своей экспертизы. Иными словами, сертификация HRCI – это контрольная точка измерения для прогресивных современных HR-ов.</p>

<p><strong>Какие есть уровни сертификации </strong><strong>HRCI. Стоимость экзаменов</strong></p>

<p>Сейчас HRCI предлагают 8 уровней сертификации HR-ов начиная от новичков и до HRD. Среди экзаменов HRCI есть те, которые основаны на американском праве и процедурах и применимы только на территории США, и международные, которые собственно интересны украинским HR-ам. К таким относятся:</p>

<ul>
	<li><strong>PHRi</strong> (Professional in Human Resources - International);</li>
	<li><strong>SPHRi </strong>(Senior Professional in Human Resources - International Professional);</li>
	<li>в этом году HRCI впервые запустили сертификационный экзамен для новичков в HR – <strong>aPHRi</strong> (Associate Professional in Human Resources - International).</li>
</ul>

<p><img alt="HRCI" src="https://dtekacademy.com/img/blog/HRCI1.jpg"></p>

<p>Мы гордимся тем, что благодаря партнерству Академии ДТЭК с HRCI пилотный проект aPHRi стартанул в Украине, и с 1 марта все желающие могут пройти сертификационный экзамен aPHRi тестинговом центре Pearson Vue.</p>

<p>В зависимости от уровня сертификации, стоимость экзамена вариируется от $300 до $495, плюс $100 за регистрацию.</p>

<p>Длительность экзамена заваисит от уровня сертификации:</p>

<ul>
	<li>aPHRi – 2 часа 15 минут,</li>
	<li>PHRi – 3 часа 15 минут,</li>
	<li>SPHRi – 2 часа 30 минут.</li>
</ul>

<p>Более детально про каждый из экзаменов мы расскажем в следующей статье.</p>

<p><strong>Подготовка к экзамену</strong></p>

<p>Строгих требований по подготовке к сертификации в HRCI нет. Главное – ваши знания и профессиональный опыт в HR направлении. Однако, существуют некие <strong>Certification Preparation Provider (CPP)</strong>, которые предоставляют онлайн или оффлайн подготовительные программы для сдачи экзаменов HRCI. Данные программы покрывают все темы, из которых состоит экзамен, и готовят HR-ов к успешному прохождению экзамена. CPPs представлены по всему миру. Подходящий можно выбрать себе в провайдеров CPP списке <a href="https://recert. HRCI.org/public/membersearch/examprepprovider?doSearch=true&amp;_s=20&amp;_o=MemberName&amp;_d"><strong>на сайте HRCI</strong></a>, среди которых есть и мы.</p>

<p>Ещё один вид подготовки к сертификации HRCI – это прохождение обучающих HR программ от Approved Providers (AP). Работает это следующим образом: образовательные компании, которые ведут программы по подготовке HR-ов сертифицируют данные программы в HRCI на предмет соответствия контенту программы контенту экзаменов HRCI. Таким образом, по итогам прохождения той или иной программы от AP вы готовы сертифицироваться в HRCI. По личному опыту скажем, что получить статус Approved Provider (AP) довольно тяжело, поскольку процесс регистрации и предоставления необходимых документов и процедур по программе очень ресурсозатратно. Однако всё реально. Совсем недавно нам удалось сертифицировать свою программу HR Business Partner, благодаря которой теперь Академия ДТЭК – HRCI Approved Provider.</p>

