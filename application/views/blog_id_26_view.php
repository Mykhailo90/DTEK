<p><strong>А с чего же начать? Начнем с начала!</strong></p>

<p>Начало, как правило, - самая стрессовая часть для большинства ораторов. И не зря. Оно задаёт тон всему выступлению.</p>

<p>- С <strong>чего </strong>мне начать?</p>

<p>- <strong>Как</strong> правильно создать первое впечатление?</p>

<p>- Как сразу "БАМ!" и в сердце???</p>

<ol>
	<li><strong>Начните до начала</strong></li>
</ol>

<p>Если есть возможность, пообщайтесь с аудиторией на неформальные темы<strong> до</strong> начала вашей официальной части. Расскажите что-то из жизни, позадавайте вопросы, пожмите руки и пр. Это создаст дополнительное доверие и снимет у вас лишнее напряжение.</p>

<ol start="2">
	<li><strong>Поехали? </strong></li>
</ol>

<p>Не приступайте сразу к теме с первых слов (Начнём, Поехали, Моя тема...).</p>

<p>Поздоровайтесь, представьтесь, зацепите внимание историей, фактом, картинкой, видео, предметом и т.п. А потом - к теме!!</p>

<p>Кстати, слишком долгое начало, тоже не подходит.</p>

<p><img alt="Делитесь энергией" src="https://dtekacademy.com/img/blog/maxresdefault.jpg"></p>

<ol start="3">
	<li><strong>Энергия</strong></li>
</ol>

<p>Начинайте с высоким уровнем энергии: живенько и эмоционально, "вялых" спикеров и так достаточно.</p>

<ol start="4">
	<li><strong>Диалог</strong></li>
</ol>

<p>Отличный способ начать с диалога с аудиторией о вашей теме.</p>

<p>Диалог должен длиться минут 5, а потом презентуйте уже свои идеи.</p>

<p>Хотя, есть риск, что то, что вы подготовили может быть уже не актуальным. Но лучше узнать об этом до, а не после презентации.</p>

<p><img alt="Выступление" src="https://dtekacademy.com/img/blog/28906923_1728491003907334_1181765227_n.jpg"></p>

<ol start="5">
	<li><strong>Интрига</strong></li>
</ol>

<p>Забросьте несколько вопросов или недосказанностей в самом начале, чтобы в течение выступления искать вместе решения. Все любят детективные истории. Пусть вам достанется роль Ватсона, а роль Шерлока отдайте людям в зале. Им это понравится.)</p>

<p>В следующей статье я выложу список инструментов удержания внимания, которые будем подробно рассматривать позже. Так вот большинство из них отлично подойдёт и для вашего начала.</p>

<p>Говорят, у<strong> вас нет второго шанса, чтобы создать первое впечатление</strong>... Я только частично с этим согласен. Не создавайте излишнюю важность происходящего. Однако при подготовке, уделите достаточное внимание <strong>НАЧАЛУ</strong> и концу вашего выступления.</p>

