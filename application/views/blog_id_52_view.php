                                    <h1>Полтора года и 11 месяцев. Путь от идеи до диплома MBA от IE Business School</h1>
                                    <div class="blog_info">
                                        <div class="author">
                                            <div>Автор статьи:</div>
                                            <div>Анастасия Тюменцева</div>
                                        </div>
