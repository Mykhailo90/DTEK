<p><strong>Что это дает Академии и госорганам?</strong></p>

<p>Можно принять позицию жалоб: на власть, законы, бедность… А можно брать и менять. Мы за второй вариант!</p>

<p>Смысл привозить инновации из США или Европы (у нас много партнеров) и консервировать их внутри компании, они никогда не заработают, если мы не будем создавать среду, развивая страну. Поэтому и выбрали такую проактивную позицию.</p>

<p>Звучит, конечно, слишком масштабно.</p>

<p>Для госучреждений партнерство с Академией – это возможность обойти целый ряд граблей, сэкономив время, деньги, человеческие ресурсы.</p>

<p>Мы очень радуемся, когда с подобными инициативами выступают представители других бизнесов. Думаю, что через пару лет подобных активистов будет много и мы станем сильной опорой для развития госсектора.</p>

