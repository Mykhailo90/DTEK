				<div class="blocks_menu" id="blocks_menu"><div class="block_area" id="block_area" style="height: auto;"><div class="swipe_block" style="transform: translateX(0%); position: relative;"><div class="blog_frame"><div class="blog_frame_940"><h1>Развиваясь, развивай! Или Зачем бизнесу тратить силы на обучение и развитие госорганов</h1><div class="blog_info"><div class="author"><div>Автор статьи:</div><div>Евгений Бондаренко</div></div><div class="category"><div>Раздел:</div><div>null</div></div><div class="date"><div>Дата:</div><div>06.06.2018</div></div></div><div class="img"><div><img src="//dtekacademy.com/img/events/20180306_101452.jpg" alt="Мининфраструктуры в Академии ДТЭК"></div></div><div class="common_text_content"><p>В Украине очень активно развивается бизнес-образование. Если 10 лет назад, например, о программах MBA знали, а тем более его получали, единицы, то сейчас уровнем своего образования и развития занимаются все, кто стремится достичь успехов в карьере.</p>

<p><strong>Развивается только бизнес? А за счет чего меняется огромная государственная машина?</strong></p>

<p>В вечерних новостях мы часто слышим о том, что Верховна Рада проголосовала за законопроект или утвердила реформу какого-то госоргана. У общества очень завышены ожидания от этих изменений. После вступления закона в силу грядет огромная работа по трансформации, которая целиком и полностью ложится на плечи этой же госструктуры.</p>

<p>Например, министерство получает указ сверху: меняемся, двигаемся теперь не так как раньше, а совсем по-другому. – Вот и все указания. И госучреждению с новым названием или вектором развития, но со старой командой и проблемами, предстоит меняться своими силами. Без поддержки и экспертизы это попросту невозможно. Было бы, конечно, здорово, если бы в Украине существовало Министерство трансформаций, но пока такой структуры нет.</p>

<p>Тогда кто и как может помочь переменам госорганов? – Ответ очевиден! Те, у кого эта экспертиза и опыт есть, – бизнес. Страна, в том числе и госсектор, может меняться, если в процесс вовлечены все стейкхолдеры. Бизнес, если хочет развиваться, должен понимать, что поддержка госучреждений на пути к переменам – это наша зона ответственности и инвестиции в экономику, в которой мы все работаем.</p>

<p><strong>В Украине есть хорошие примеры трансформаций госорганов</strong></p>

<p>Принципы работы госорганов должны быть аналогичны бизнесу, ведь госучреждения – это сервисные компании, которые оказывают услуги населению. У нас уже есть хорошие примеры “клиентоориентированности”:</p>

<ul>
	<li><strong>сервис IGov</strong> – пока силами активистов из бизнеса, но уже упростил гору задач населению;</li>
	<li>единые сервисные центры обслуживания граждан – удобное решение ряда задач в 1 месте;</li>
	<li><strong>“Документ”</strong> – хоть и много жалоб на очереди за паспортами, но в стенах учреждений ощущаешь себя клиентом, а не заложником бюрократической машины (поэтому с довольным лицом соглашаешься получить свой биометрический паспорт через 2 месяца);</li>
	<li><strong>Нацполиция</strong> – это продукт многих улучшений и ошибок, можно много критиковать, но, согласитесь, что прошлая милиция – это прошлый век. И тут мы горды, что тоже сделали свой вклад в развитие этой новой структуры.</li>
</ul>

