<p><strong>Что нужно для перемен?</strong></p>

<p>Думаю, что самый яркий пример – Сингапур. Он показывает, что такие большие изменения в стране может сделать только лидер с большой идеей – должен быть визионер. Но для нас очевидно, что самому лидеру с этим точно не справится. Лидер – это необходимое условие, но недостаточное, ему нужна еще команда.</p>

<p>Именно поэтому есть много людей из бизнеса, которые пошли в политику и не смогли внедрить изменения, потому что никто из них не создал команду. Все ждут, что они придут и перед ними будут открываться двери, а потом сетуют: “Это большая махина, там все свои, поэтому у меня не получилось…” Причина неудач бизнесменов в политике – отсутствие критической массы для внедрения изменений. Каким бы сильным не был лидер и его идея, он сам не справится.</p>

<p><strong><em>2 условия успеха государства: лидерство и команда – мы как бизнес в них сильны. Поэтому мы и делимся своим опытом с представителями госорганов.</em></strong></p>

<p><strong>А какая выгода для компании от развития госучреждений?</strong></p>

<p>Академия ДТЭК – это в первую очередь корпоративный университет для 70 000 сотрудников, но несколько лет назад мы стали делиться своими знаниями с внешним рынком, так как ряд сложностей одинаково актуален для всех бизнесов в Украине: HR, менеджмент, безопасность и энергоэффективность на предприятиях.</p>

<p>Но, когда мы вышли на рынок, начали оценивать уровень компетенций в этих направлениях, то увидели, что больше всех страдают госорганы.</p>

<p>Будем честными, не только альтруизм руководил нами при подписании первых меморандумов о сотрудничестве с госорганами, мы очень гордились, что нам доверяют такие важные структуры и даже позволяли себе немного хвастаться этим.</p>

<p>Еще эта поддержка государства полностью отвечает нашей философии. В момент создания Академии один из журналистов задал генеральному директору ДТЭК вопрос: “А вот вы себе обучите людей, вложите в них деньги, а они уйдут. Что вы с этим планируете делать?”</p>

<p>Гендиректор дал простой ответ, который и тогда, и сейчас отображает философию создания корпоративного университета:</p>

<p>“Мы, конечно же, хотим, чтоб эти люди остались работать у нас после обучения. Нам бы очень не хотелось, но мы тоже будем рады, ведь не можем никого приковать золотыми цепями к компании, если они останутся работать в Украине и уйдут работать в другие компании.</p>

<p>Нам будет очень обидно, если эти люди уедут заграницу, посчитают, что в нашей стране им нет применения и тд.</p>

<p>Мы все равно будем развивать наших сотрудников, независимо от любого из этих 3 исходов, стараясь оставить людей в компании или в Украине.”</p>

<p>Это некий социальный вклад компании, так как люди, которые у нас или в другой компании, работают в Украине, платят здесь налоги.</p>

<p>Наш проект работы с госорганами – это продолжение вот этой философии. Почему бы нам не поделиться с коллегами из госорганов тем, что мы знаем и умеем делать лучше всего.</p>

<div class="blog_img_3X4"><img alt="Государство и общество" src="https://dtekacademy.com/img/events/DSC_1303.jpg"></div>

<p>Давайте мыслить логично: чем сильнее будут развиты менеджеры, тем сильнее будет развита компания/госучреждение, соответственно и бизнес в Украине, чем сильнее бизнес внутри, тем более он конкурентен на глобальных рынках и в других странах. Чем сильнее бизнес = тем сильнее страна. Сильная конкуренция – лучше качество + выигрыш для клиента и рынка в целом.</p>

<p><strong>С какими госучреждениями вы сотрудничаете?</strong></p>

<p>Сегодня мы сотрудничаем с:</p>

<ul>
	<li>Национальным банком Украины;</li>
	<li>Укрпоштой;</li>
	<li>Министерством финансов Украины;</li>
	<li>Министерством энергетики и угольной промышленности Украины;</li>
	<li>Министерством инфраструктуры Украины;</li>
	<li>11 вузами и еще целым рядом учреждений.</li>
</ul>

<p><strong>Какой экспертизой нужно делиться?</strong></p>

<p>Главное правило – обучать тому, в чем вы на самом деле сильны! Это для тех, кто тоже захочет приобщиться к развитию госорганов. Только проверенные навыки!</p>

<p>О нашем опыте: когда мы заключаем новое партнерство, то обязательно помогаем разобраться госоргану, с чего стоит начать развитие своих сотрудников:</p>

<ul>
	<li>Кому-то в первую очередь нужно развивать функцию менеджмента;</li>
	<li>Другие госпредприятия нужно готовить к переменам и трансформациям;</li>
	<li>Некоторым нужно прокачать командообразование;</li>
	<li>А у кого-то острая потребность в стратегической сессии и планировании.</li>
</ul>

<p>Это не значит, что мы берем всех сотрудников и обучаем этим навыкам. Мы обучаем небольшие группы, которые потом приносят эти скилы в свою команду и делятся ими.</p>

<p><strong>Вузы: 11 университетов-партнеров и 9 лет работы</strong></p>

<p>Тут мы буквально “собаку съели”. Идея очень простая – максимально приблизить ту теорию, которую дают в вузах к той практике, которая есть на предприятиях. И речь не только о студентах, но и о преподавателях, потому что далеко не все были на реальных предприятиях и видели реальное оборудование, или были там еще в свои студенческие годы.</p>

<p>И при такой ситуации оторванности вузов от практики наши вузы не проявляли инициативы. Мы начали сотрудничество с университетами еще в 2009 году. Я часто задавал вопрос: а кто вам раньше мешал к нам обратиться и попроситься приехать на предприятие? Мы сами первые начали выходить на вузы. Они преимущественно, если обращались, то в основном за финансовой поддержкой. А с идеями с точки зрения развития обращались очень редко.</p>

<p>Сейчас эта картинка поменялась: реформы Минобразования (оптимизация, сокращение финансирования и др.) заставили их зашевелиться. Раньше они не беспокоились о том, сколько лет им еще работать, ведь государство давало деньги. Сейчас им нужно себя отстаивать перед министерством, отвоевывать бюджеты. Министерство будет оставлять лучших и финансировать только лучших. Такой подход их взбудоражил. Мне стали часто писать в социальных сетях. Я стал видеть представителей вузов на разных конференциях. И это очень хорошая тенденция. Да и сами студенты инициируют сотрудничество, организовываются и приобщают преподавателей.</p>

